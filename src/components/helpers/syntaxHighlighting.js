export const createExtensionTranslator =
  (mapping, defaultDefault = null) =>
  (ext, defaultValue = defaultDefault) => {
    ext = ext.trim().toLowerCase();
    if (ext === '') {
      return mapping.makefile; // makefile has no extension
    } else if (mapping[ext]) {
      return mapping[ext]; // mapping found
    } else {
      return defaultValue;
    }
  };

